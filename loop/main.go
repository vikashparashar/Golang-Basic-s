// Loops In Golang
// Go program to illustrate the
// use of simple for loop
package main

import "fmt"

// Main function
func main() {

	// for loop
	// This loop starts when i = 0
	// executes till i<4 condition is true
	// post statement is i++
	for i := 0; i < 4; i++ {
		fmt.Println(" Vikash Parashar ")
	}

}

// for loop can also be used as while loop in golang
